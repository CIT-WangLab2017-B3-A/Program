# LSM303D -3D compass (3D accelerometer & 3D magnetometer)
A_DATA_RATE=0x05
#0x00-0x0A[bit]
#3.125-6.25-12.5-25.0-50-100-200-400-800-1600[Hz]
CTRL_1 = (A_DATA_RATE<<4) | 0x0007

A_LOW_PASS_FILTER=0b00
#00-01-10-11
#773-194-362-50[Hz]
A_FULL_SCALE=0b010
#000-001-010-011-100
#+-2-4-6-8-16[g]
CTRL_2 = (A_LOW_PASS_FILTER<<6)|(A_FULL_SCALE<<3)

M_RESOLUTION=0b11
#00-11
#LO-HI
M_DATA_RATE=0b001
#000-001-010-011-100
#3.125-6.25-12.5-25-50[Hz]
CTRL_5 = (M_RESOLUTION<<5)|(M_DATA_RATE<<2)

M_FULL_SCALE=0b01
#00-01-10-11
#+-2-4-8-12[gauss]
CTRL_6 = M_FULL_SCALE<<5

CTRL = [0x00, CTRL_1, CTRL_2, 0x00, 0x00, CTRL_5, CTRL_6, 0x00]